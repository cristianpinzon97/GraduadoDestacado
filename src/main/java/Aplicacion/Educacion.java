package Aplicacion;

/**
 *
 * @author crist
 */
public class Educacion {
    private String nombreLugar, nombreCurso, periodo, actividades, detallesExtra;


    public String getNombreLugar() {
        return nombreLugar;
    }

    public void setNombreLugar(String nombreLugar) {
        this.nombreLugar = nombreLugar;
    }

    public String getNombreCurso() {
        return nombreCurso;
    }

    public void setNombreCurso(String nombreCurso) {
        this.nombreCurso = nombreCurso;
    }

    public String getPeriodo() {
        return periodo;
    }

    public void setPeriodo(String periodo) {
        this.periodo = periodo;
    }

    public String getActividades() {
        return actividades;
    }

    public void setActividades(String actividades) {
        this.actividades = actividades;
    }

    public String getDetallesExtra() {
        return detallesExtra;
    }

    public void setDetallesExtra(String detallesExtra) {
        this.detallesExtra = detallesExtra;
    }
    
    
}
